    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked

        JFileChooser f = new JFileChooser();
        if (f.showOpenDialog(InserimentoProdotto.this) == JFileChooser.APPROVE_OPTION) {
            pressed = true;
            File fi = f.getSelectedFile();
            try {
                p.setImmProdotto(fi.getCanonicalPath());
            } catch (IOException ex) {
                Logger.getLogger(InserimentoProdotto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void Initialize(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_Initialize
        ButtonGroup group = new ButtonGroup();
        group.add(jRadioButton1);
        group.add(jRadioButton2);
        jButton1.setFocusCycleRoot(false);
        jButton3.setFocusCycleRoot(false);
        file.setPath(path);
        try {
            pr = file.ReadFile();
            if (pr.size() == 20) {
                JOptionPane.showMessageDialog(null, "Lista prodotti completa", "TUTTO PIENO :(", JOptionPane.WARNING_MESSAGE);
                FrameProdotto ip = new FrameProdotto();
                ip.setVisible(true);
                this.setVisible(false);
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(InserimentoProdotto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_Initialize

