    private void jToggleButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jToggleButton1MouseClicked
         if (jRadioButton1.isSelected() == false && jRadioButton2.isSelected() == false || jTextField2.getText().length() == 0 || jTextField3.getText().length() == 0 || pressed == false) {
            JOptionPane.showMessageDialog(null, "Compila tutti i campi", "INCOMPLETO :(", JOptionPane.WARNING_MESSAGE);
        } else {
            p.setNomeProdotto(jTextField2.getText());
            p.setPrezzo(jTextField3.getText());
            if (jRadioButton1.isSelected() == true) {
                p.setMaggiorenne(false);
                 p.setScorte(40);
            } else if (jRadioButton2.isSelected() == true) {
                p.setMaggiorenne(true); 
                p.setScorte(20);
            }
            FileManagment manage = new FileManagment();
            manage.setPath(path);
            try {
                manage.WriteToFile(manage.toCSS(p));
                p = new Prodotto();
                jRadioButton1.setSelected(false);
                jRadioButton2.setSelected(false);
                jTextField2.setText("");
                jTextField3.setText("");
                 JOptionPane.showMessageDialog(null, "Inserito", ":)", JOptionPane.OK_OPTION);
            } catch (IOException ex) {
                Logger.getLogger(InserimentoProdotto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jToggleButton1MouseClicked

